<?php
//----------------------------------------------------------------------------------------------------------------------
namespace SetBased\Abc\CompanyResolver;

//----------------------------------------------------------------------------------------------------------------------
/**
 * Company resolver for website with a single domain only.
 */
class UniCompanyResolver implements CompanyResolver
{
  //--------------------------------------------------------------------------------------------------------------------
  /**
   * The ID of the company.
   *
   * @var int
   */
  private $cmpId;

  //--------------------------------------------------------------------------------------------------------------------
  /**
   * Object constructor.
   *
   * @param int $cmpId The ID of the company.
   *
   * @api
   * @since 1.0.0
   */
  public function __construct($cmpId)
  {
    $this->cmpId = $cmpId;
  }

  //--------------------------------------------------------------------------------------------------------------------
  /**
   * Returns the ID of the company.
   *
   * @return int
   *
   * @api
   * @since 1.0.0
   */
  public function getCmpId()
  {
    return $this->cmpId;
  }

  //--------------------------------------------------------------------------------------------------------------------
}

//----------------------------------------------------------------------------------------------------------------------
